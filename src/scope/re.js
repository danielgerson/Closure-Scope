// Reasignación, Redeclaración

var firstName; // Declarando, se le asigna undefined
firstName = 'Daniel';
console.log(firstName);

var lastName = 'Barrios' // Declarando y asignando valor
lastName = 'Navarro'; // Reasignando nuevo valor a lastName
console.log(lastName);

var secondName = 'Santi'; // Declarando y asignando valor
var secondName = 'Lucas'; // Reasignando valor a secondName
console.log(secondName)

// Let
let fruit = 'Apple'; // Declarando y asignando valor
fruit = 'Kiwi'; // Reasignando valor
console.log(fruit);
// let fruit = 'Banana';

// const 
const animal = 'Dog'; // Declara y reasignando
animal = 'Cat';
console.log(animal);

const vehicles = [];
vehicles.push('BMW');
console.log(vehicles);